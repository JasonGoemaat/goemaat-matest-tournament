export const generateTimes32 = () => {
  const addTimes = (arr: number[], timeStr: string, count: number) => {
    arr = arr || [];
    let time = new Date(timeStr);
    let timeNumber = time.getTime();
    for (let i = 0; i < count; i++) {
      arr.push(timeNumber);
      timeNumber += 20 * 60 * 1000;
    }
    return arr;
  }

  const arr: number[] = [];
  addTimes(arr, '2022-02-04 17:00:00-06', 16); // 16 games friday night
  addTimes(arr, '2022-02-05 08:00:00-06', 50); // another 50 on Saturday
  return arr;
}

